package ex03;


public class Medicamento {
    private int data_atual;
    private int data_fabric;
    private int validade;

    public int getData_atual() {
        return data_atual;
    }

    public void setData_atual(int data_atual) {
        this.data_atual = data_atual;
    }

    public int getData_fabric() {
        return data_fabric;
    }

    public void setData_fabric(int data_fabric) {
        this.data_fabric = data_fabric;
    }

    public int getValidade() {
        return validade;
    }

    public void setValidade(int validade) {
        this.validade = validade;
    }
}
